class MaxCalculator {
    fun max(a: Int, b: Int): Int {
        if (a > b) {
            return a
        } else {
            return b
        }
    }
}

class MinCalculator {
    fun min(a: Int, b: Int): Int {
        if (a > b) {
            return a
        } else {
            return b
        }
    }
}
